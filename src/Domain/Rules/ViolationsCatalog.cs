    private static Dictionary<int, ViolationDef>? _map;

    public static List<ViolationDef> GetAll() => All;

    public static ViolationDef? GetById(int id)
    {
        _map ??= All.ToDictionary(v => v.Id);
        return _map.GetValueOrDefault(id);
    }

    /// <summary>إرجاع الدرجة الفعلية حسب المرحلة — مطابق لـ getEffectiveDegree_ في Server_Data.gs سطر 351-357</summary>
    public static int GetEffectiveDegree(int violationId, string? stage)
    {
        var v = GetById(violationId);
        if (v == null) return 1;
        if (stage == "ابتدائي" && v.DegreeElementary.HasValue)
            return v.DegreeElementary.Value;
        return v.Degree;
    }
